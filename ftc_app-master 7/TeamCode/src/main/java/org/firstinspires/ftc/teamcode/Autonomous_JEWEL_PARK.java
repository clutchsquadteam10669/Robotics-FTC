////        robot.rightFrontMotor.setPower(-TURN_SPEED);
////        robot.rightBackMotor.setPower(-TURN_SPEED);
////        encoderDrive(TURN_SPEED, .25, -.25, 2.6);  // Forward with 3 Sec timeout
////        encoderDrive(DRIVE_SPEED, .25, .25, 2.6);  // Forward with 3 Sec timeout
//        runtime.reset();
